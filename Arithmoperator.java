public class Arithmoperator {
    public static void main(String args[]) {  
        int a = 10;  // Declare and initialize variable 'a' with the value 10
        int b = 5;   // Declare and initialize variable 'b' with the value 5
        
        // Addition
        System.out.println(a + b); // 10 + 5 = 15
        // The result is 15 because 'a' and 'b' are added together.

        // Subtraction
        System.out.println(a - b); // 10 - 5 = 5
        // The result is 5 because 'a' is subtracted by 'b'.

        // Multiplication
        System.out.println(a * b); // 10 * 5 = 50
        // The result is 50 because 'a' is multiplied by 'b'.

        // Division
        System.out.println(a / b); // 10 / 5 = 2
        // The result is 2 because 'a' is divided by 'b'.

        // Modulus (Remainder)
        System.out.println(a % b); // 10 % 5 = 0
        // The result is 0 because when 'a' is divided by 'b', there is no remainder.

    }
} 