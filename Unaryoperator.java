public class Unaryoperator {
    public static void main(String args[]) {  
        int x = 10;  // Initialize 'x' with the value 10

        System.out.println(x++);  // Post-increment: Print 'x' (10) and then increment it to 11
        System.out.println(++x);  // Pre-increment: Increment 'x' to 12 and then print it
        System.out.println(x--);  // Post-decrement: Print 'x' (12) and then decrement it to 11
        System.out.println(--x);  // Pre-decrement: Decrement 'x' to 10 and then print it
    }
} 
